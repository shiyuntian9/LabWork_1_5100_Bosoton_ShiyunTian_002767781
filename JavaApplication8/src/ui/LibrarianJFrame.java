    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(business.getMasterBorrowDirectory().getMasterBorrowDirectory().size() > 0){
            viewTableModel.setRowCount(0);
            
            for(BorrowOrder bo: business.getMasterBorrowDirectory().getMasterBorrowDirectory()){
                if(bo.getStatus().equals("pending")){
                    Object row[]  =new Object[3];
                    row[0] = bo;
                    row[1] = bo.getBook().getName();
                    row[2] = bo.getDuration();
                    viewTableModel.addRow(row);
                }
                
                
            }
            }else{
            JOptionPane.showMessageDialog(null, "No pending case!");
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        int selectedRow =jTable1.getSelectedRow();
        
        if(selectedRow >= 0 ){
        BorrowOrder bo  = (BorrowOrder)jTable1.getValueAt(selectedRow, 0);
        bo.setStatus("Accepted");
        bo.getUserAccount().findCustomer().getBorrowOrder(bo).setStatus("Accepted");
        
        
        
        // TODO add your handling code here:
    }//GEN-LAST:event_acceptButtonActionPerformed
    }
    
